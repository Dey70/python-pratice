alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm',
            'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def caesar_cipher(original_text, shift_amount, encode_or_decode):
    if encode_or_decode == "decode":
        shift_amount *= -1

    plain_text = ""

    for letter in original_text:
        if letter not in alphabet:
            plain_text += letter
        else:
            shifted_location = (alphabet.index(letter) + shift_amount) % len(alphabet)
            plain_text += alphabet[shifted_location]

    print(f"The {encode_or_decode}d text is: {plain_text}")


should_continue = True

while should_continue:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n").lower()
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))

    caesar_cipher(original_text=text, shift_amount=shift, encode_or_decode=direction)

    restart = input("Type 'yes' to continue or 'no' to exit:\n").lower()
    if restart == "no":
        should_continue = False
        print("Goodbye!")
    elif restart != "yes":
        print("Invalid input, exiting.")
        should_continue = False
